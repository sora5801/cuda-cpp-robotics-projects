// ===========================================================================
// mc_tables.h — the marching-cubes case tables for project 05.01
//               (shared verbatim by the GPU kernel and the CPU recount)
//
// What lives here and why it is a header
// --------------------------------------
// Marching cubes owes its speed to a PRECOMPUTED answer to "which triangles
// does a cell with THIS in/out corner pattern produce?" — 256 possible
// patterns (8 corners, each inside or outside), each mapping to at most 5
// triangles whose vertices lie on the cell's 12 edges. Deriving the table
// by hand from the 15 base cases + symmetries is a classic exercise
// (Lorensen & Cline 1987 did it once so nobody has to again); using the
// published table is the standard practice this project follows.
//
// The table data below is the canonical triangulation table as published in
// Paul Bourke's widely reproduced "Polygonising a scalar field" reference
// (1994; the same table ships in NVIDIA's CUDA marching-cubes sample, VTK,
// and countless engines). It is data, not code — reproduced here with
// provenance per CLAUDE.md §4.1 (credit, don't copy code wholesale; the
// KERNEL around this table is this project's own teaching implementation).
//
// The initializer is wrapped in a MACRO so the one authoritative blob can
// initialize BOTH homes it needs (initializer lists cannot be assigned from
// another array at namespace scope):
//   * kernels.cu:        __constant__ device copy — every thread of a warp
//                        reads the SAME row for equal case indices, the
//                        broadcast access pattern __constant__ memory is
//                        built for (the 09.01 lesson; 4 KiB of 64 KiB used);
//   * reference_cpu.cpp: a plain constexpr host copy for the exact recount.
// One blob, two copies, zero chance of divergence.
//
// CONVENTIONS (must match kernels.cu / reference_cpu.cpp exactly):
//   Corner numbering (unit cell, +x right, +y in, +z up):
//        4----------5          corner 0 = (0,0,0)   corner 4 = (0,0,1)
//       /|         /|          corner 1 = (1,0,0)   corner 5 = (1,0,1)
//      7----------6 |          corner 2 = (1,1,0)   corner 6 = (1,1,1)
//      | |        | |          corner 3 = (0,1,0)   corner 7 = (0,1,1)
//      | 0--------|-1
//      |/         |/           Case index bit i is SET when corner i is
//      3----------2            INSIDE the surface (tsdf < 0).
//   Edge numbering (the 12 cell edges, each joining two corners):
//        e0=(0,1) e1=(1,2) e2=(2,3)  e3=(3,0)     bottom face (z=0)
//        e4=(4,5) e5=(5,6) e6=(6,7)  e7=(7,4)     top face    (z=1)
//        e8=(0,4) e9=(1,5) e10=(2,6) e11=(3,7)    vertical pillars
//
// Each of the 256 rows lists up to 5 triangles as flat edge-index triples,
// terminated/padded with -1 to a fixed width of 16 (so row lookup is simple
// arithmetic — the last slot is always -1 because 5 triangles use 15).
// Triangle winding is the table's standard orientation (counter-clockwise
// seen from OUTSIDE, i.e., from tsdf > 0); some viewers still need
// double-sided rendering — noted in demo/README.md.
//
// This table was machine-checked before being committed (the check script's
// logic is described in THEORY.md §verification): every listed edge joins
// one inside and one outside corner for its case, every sign-change edge of
// every case is used, complementary cases (c, 255-c) cut identical edge
// sets, and the total triangle count across all cases is the published 820.
//
// Read this after: kernels.cuh.  Used by: kernels.cu, reference_cpu.cpp.
// ===========================================================================
