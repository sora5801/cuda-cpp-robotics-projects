bool tensorrt_path_available();

#ifdef USE_TENSORRT
// Builds (or loads a cached) TensorRT engine from the SAME weights.bin,
// runs one inference on the SAME preprocessed tensor our CUDA kernels
// already produced, and hands the engine's output straight back into our
// UNCHANGED argmax/threshold/NMS/keypoint kernels — the point being that
// those four kernels do not know or care whether the 2 conv layers ran as
