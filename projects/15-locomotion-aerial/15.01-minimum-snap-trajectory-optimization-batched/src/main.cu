// ===========================================================================
// main.cu — entry point for project 15.01
//           Minimum-snap trajectory optimization batched over waypoint sets
//           (quadrotor-style 2-D waypoint flight)
//
// What this program does, start to finish
// ---------------------------------------
//   1. Print the banner + GPU info + PROBLEM line (batch size, segment
//      shape, box/spacing parameters).
//   2. SAMPLE STAGE: load the committed hand-designed waypoint sets from
//      data/sample/ (a handful of named sets incl. one literally called
//      "slalom" — required, used for the artifact) with a strict loader.
//   3. BATCH: fold the sample sets into the front of a K-set batch (default
//      K=10,000) and fill the remainder with waypoint sets generated by a
//      seeded xorshift32 RNG, bounded to a box with a minimum spacing
//      between consecutive waypoints.
//   4. GPU + CPU: solve the WHOLE batch on both paths (kernels.cu,
//      reference_cpu.cpp) — this project has no closed loop, so unlike
//      08.01 the "compute" step is a single one-shot batched call, the
//      33.01/09.01 shape.
//   5. VERIFY (the §5 gate): GPU coefficients vs CPU coefficients,
//      elementwise, within a documented tolerance.
//   6. CONSTRAINTS (verification against the MATHEMATICAL DEFINITION, not
//      against the CPU oracle): for EVERY set in the batch, re-evaluate the
//      solved polynomials via an INDEPENDENT code path (eval_segment_derivs
//      below, double precision, its own loops) and measure (a) waypoint
//      interpolation error, (b) endpoint zero-derivative error, (c)
//      interior continuity jumps, plus (d) the analytic snap-cost integral
//      (finite and non-negative for every set).
//   7. ARTIFACT: dense-sample the "slalom" set's trajectory (position,
//      velocity, acceleration vs time) into demo/out/trajectory.csv, and
//      rasterize the 2-D path into demo/out/slalom_path.pgm.
//   8. RESULT: PASS iff VERIFY and CONSTRAINTS both hold.
//
// Output contract (load-bearing!): stable lines "[demo]", "PROBLEM:",
// "SAMPLE:", "VERIFY:", "CONSTRAINTS:", "ARTIFACT:", "RESULT:" — no
// machine-dependent numbers in any of them (those live in "[info]"/"[time]"
// lines, deliberately unchecked). Change a stable line here => update
// demo/expected_output.txt in the same change, and vice versa.
//
// Read this first, then kernels.cuh (the contract) -> reference_cpu.cpp
// (the oracle) -> kernels.cu (the GPU solve).
// ===========================================================================

