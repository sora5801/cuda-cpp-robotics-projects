// ---------------------------------------------------------------------------
// Verification tolerances — documented constants, not tuned per run. Every
// number here is justified in THEORY.md §how-we-verify-correctness; the
// measured worst-case values (well inside these) are printed on "[info]"
// lines so a reader can see the real margin, not just the pass/fail bit.
// ---------------------------------------------------------------------------
// Measured worst-case values on the reference machine (RTX 2080 SUPER,
// K=10000, both axes, all constraint categories — printed on "[info]"
// lines every run): relative coeff deviation ~6.4e-4, interpolation
// ~5.1e-5 m, endpoint derivative ~2.3e-4, continuity (relative) ~5.0e-4.
// Every tolerance below carries at least an order of magnitude of
// headroom above that measurement — enough that a genuine indexing,
// pivoting, or layout bug (which shifts results at O(1), not at the
// FP32-rounding scale) trips the gate immediately, while ordinary
// architecture-to-architecture FMA/rounding differences do not.
static const float kVerifyRelTol       = 5e-3f;   // GPU-vs-CPU coefficient agreement (relative, floor 1)
static const float kTolInterpM         = 1e-3f;   // waypoint interpolation residual, meters
static const float kTolEndpointDeriv   = 1e-2f;   // |velocity|,|accel|,|jerk| at the two trajectory ends
static const float kTolContinuityRel   = 1e-2f;   // interior derivative jump, RELATIVE (floor 1) — see THEORY.md
static const double kCostNonNegFloor   = -1e-3;   // snap cost must be >= this (tiny slack for FP roundoff)

// ---------------------------------------------------------------------------
// xorshift32 / uniform01 — the repo's portable deterministic RNG (same
// generator 08.01 uses for its exploration noise). Used here ONCE, to fill
// the batch beyond the hand-designed sample sets — see generate_random_
// waypoint_set below.
// ---------------------------------------------------------------------------
static inline uint32_t xorshift32(uint32_t& state)
{
    state ^= state << 13;
    state ^= state >> 17;
    state ^= state << 5;
    return state;
}

static inline float uniform01(uint32_t& state)   // [0,1)
{
    return (xorshift32(state) >> 8) * (1.0f / 16777216.0f);
}

// ---------------------------------------------------------------------------
// generate_random_waypoint_set — draw kNumWaypoints (x,y) points uniformly
// from a [-box_half,box_half]^2 square, rejecting a draw if it lands closer
// than min_spacing to the PREVIOUS waypoint (rejection sampling; capped at
// 200 attempts, which in practice never binds — see THEORY.md for the
// acceptance-probability arithmetic). This is what makes "10,000 random
// waypoint sets" a well-posed batch of trajectory problems instead of a
// batch of degenerate near-duplicate points.
// ---------------------------------------------------------------------------
static void generate_random_waypoint_set(uint32_t& rng, float box_half, float min_spacing,
                                         float wp[kWaypointFloatsPerSet])
{
    for (int i = 0; i < kNumWaypoints; ++i) {
        float x = 0.0f, y = 0.0f;
        int attempts = 0;
        do {
            x = -box_half + 2.0f * box_half * uniform01(rng);
            y = -box_half + 2.0f * box_half * uniform01(rng);
            ++attempts;
        } while (i > 0 && attempts < 200 &&
                std::hypot(x - wp[(i - 1) * 2 + 0], y - wp[(i - 1) * 2 + 1]) < min_spacing);
        wp[i * 2 + 0] = x;
        wp[i * 2 + 1] = y;
    }
}

// ---------------------------------------------------------------------------
// Committed sample loader — a handful of HAND-DESIGNED, NAMED waypoint sets
// (data/sample/waypoint_sets.csv), strict format, exe-relative path
// resolution (the 08.01 / 07.09 pattern). Format:
//
//     SET,<name>              one line, opens a new set
//     WP,<x_m>,<y_m>          exactly kNumWaypoints of these follow
//
// Unknown labels, a WP row before any SET row, or a SET with the wrong
// number of WP rows all abort the load loudly (CLAUDE.md §8: never a
// silent fallback on malformed data).
// ---------------------------------------------------------------------------
struct SampleSets {
    std::vector<std::string> names;
    std::vector<float> waypoints;   // names.size() * kWaypointFloatsPerSet floats
    bool loaded = false;
};

static SampleSets load_sample_sets(const std::string& path)
{
    SampleSets out;
    std::ifstream in(path);
    if (!in.is_open()) return out;

    std::string line, current_name;
    int wp_in_current = 0;
    bool have_open_set = false;

    while (std::getline(in, line)) {
        if (!line.empty() && line.back() == '\r') line.pop_back();   // tolerate CRLF readers
        if (line.empty() || line[0] == '#') continue;

        std::stringstream ss(line);
        std::string label;
        std::getline(ss, label, ',');

        if (label == "SET") {
            if (have_open_set && wp_in_current != kNumWaypoints) {
                std::fprintf(stderr, "sample sets: SET '%s' had %d WP rows, expected %d\n",
                            current_name.c_str(), wp_in_current, kNumWaypoints);
                return SampleSets{};
            }
            if (!std::getline(ss, current_name, ',')) {
                std::fprintf(stderr, "sample sets: SET row missing a name\n");
                return SampleSets{};
            }
            out.names.push_back(current_name);
            out.waypoints.resize(out.waypoints.size() + kWaypointFloatsPerSet, 0.0f);
            wp_in_current = 0;
            have_open_set = true;
        } else if (label == "WP") {
            if (!have_open_set) {
                std::fprintf(stderr, "sample sets: WP row before any SET row\n");
                return SampleSets{};
            }
            if (wp_in_current >= kNumWaypoints) {
                std::fprintf(stderr, "sample sets: SET '%s' has more than %d WP rows\n",
                            current_name.c_str(), kNumWaypoints);
                return SampleSets{};
            }
            std::string xs, ys;
            if (!std::getline(ss, xs, ',') || !std::getline(ss, ys, ',')) {
                std::fprintf(stderr, "sample sets: malformed WP row '%s'\n", line.c_str());
                return SampleSets{};
            }
            const int set_idx = static_cast<int>(out.names.size()) - 1;
            const size_t base = static_cast<size_t>(set_idx) * kWaypointFloatsPerSet
                              + static_cast<size_t>(wp_in_current) * 2;
            out.waypoints[base + 0] = std::strtof(xs.c_str(), nullptr);
            out.waypoints[base + 1] = std::strtof(ys.c_str(), nullptr);
            ++wp_in_current;
        } else {
            std::fprintf(stderr, "sample sets: unknown row label '%s'\n", label.c_str());
            return SampleSets{};
        }
    }
    if (have_open_set && wp_in_current != kNumWaypoints) {
        std::fprintf(stderr, "sample sets: final SET '%s' had %d WP rows, expected %d\n",
                    current_name.c_str(), wp_in_current, kNumWaypoints);
        return SampleSets{};
    }
    if (out.names.empty()) {
        std::fprintf(stderr, "sample sets: no SET rows found in %s\n", path.c_str());
        return SampleSets{};
    }
    out.loaded = true;
    return out;
}

static std::string project_root_from(const char* argv0)
{
    std::string exe(argv0 ? argv0 : "");
    size_t cut = exe.find_last_of("/\\");
    if (cut == std::string::npos) return ".";
    return exe.substr(0, cut) + "/../../..";
}

static std::string find_file(const std::string& cli_path, const char* argv0, const std::string& rel)
{
    std::vector<std::string> candidates;
    if (!cli_path.empty()) candidates.push_back(cli_path);
    candidates.push_back(project_root_from(argv0) + "/" + rel);
    candidates.push_back(rel);
    candidates.push_back("../" + rel);
    for (const auto& c : candidates)
        if (std::ifstream(c).is_open()) return c;
    return "";
}

static bool ensure_dir(const std::string& path)
{
#ifdef _WIN32
    const int r = _mkdir(path.c_str());
#else
    const int r = mkdir(path.c_str(), 0755);
#endif
    return r == 0 || errno == EEXIST;
}

// ---------------------------------------------------------------------------
// eval_segment_derivs — evaluate ONE segment's polynomial and its
// derivatives d = 0..kMaxContinuityDeriv at a given tau, in DOUBLE
// precision, via loops written INDEPENDENTLY of assemble_minsnap_system's
// falling-factorial helper in kernels.cu/reference_cpu.cpp.
//
// Why independent, and why double: this function is the backbone of the
// CONSTRAINTS check below — "does the solved trajectory actually satisfy
// the mathematical definition it was solved for". If it reused the exact
// same helper the assembly step uses, a bug present in BOTH the assembly
// and this check could cancel out and hide (the same failure mode that
// motivates 08.01's two independent verification stages). Double
// precision here means any residual we measure is attributable to the
// FP32 SOLVE, not to additional error introduced by the check itself.
// ---------------------------------------------------------------------------
static void eval_segment_derivs(const float c[kCoeffsPerSegment], double tau,
                                double out[kMaxContinuityDeriv + 1])
{
    for (int d = 0; d <= kMaxContinuityDeriv; ++d) {
        double acc = 0.0;
        for (int j = d; j < kCoeffsPerSegment; ++j) {
            double coeff = 1.0;
            for (int t = 0; t < d; ++t) coeff *= static_cast<double>(j - t);
            double tp = 1.0;
            for (int p = 0; p < (j - d); ++p) tp *= tau;
            acc += coeff * static_cast<double>(c[j]) * tp;
        }
        out[d] = acc;
    }
}

// ---------------------------------------------------------------------------
// segment_snap_integral — the analytic integral of (d^4p/dtau^4)^2 over
// tau in [0,1] for one segment's coefficients. p'''' is a CUBIC in tau
// (24c4 + 120c5 tau + 360c6 tau^2 + 840c7 tau^3 — the 4th derivative kills
// c0..c3), so its square is a degree-6 polynomial with an EXACT closed-form
// integral: sum_{m,n=0}^{3} a_m a_n / (m+n+1) (the standard moment-integral
// identity int_0^1 tau^(m+n) dtau = 1/(m+n+1)). No numerical quadrature is
// needed anywhere in this project's cost sanity check.
// ---------------------------------------------------------------------------
static double segment_snap_integral(const float c[kCoeffsPerSegment])
{
    const double a[4] = {
        24.0  * static_cast<double>(c[4]),
        120.0 * static_cast<double>(c[5]),
        360.0 * static_cast<double>(c[6]),
        840.0 * static_cast<double>(c[7]),
    };
    double integral = 0.0;
    for (int m = 0; m < 4; ++m)
        for (int n = 0; n < 4; ++n)
            integral += a[m] * a[n] / static_cast<double>(m + n + 1);
    return integral;
}

// compute_snap_cost — total (both axes, all segments) physical snap-squared
// integral for one waypoint set, seconds^-7 * meters^2 units (an unusual
// but perfectly well-defined physical unit for "integral of (m/s^4)^2 dt").
// Scaling by 1/T_seg^7 converts the tau-domain integral above into real
// time: dt = T_seg dtau, and the 4th time-derivative carries a 1/T_seg^4
// factor, so the SQUARED integral picks up 1/T_seg^8 from squaring the
// derivative and T_seg from the dt->dtau change of variable: net 1/T_seg^7.
static double compute_snap_cost(const float set_coeffs[kCoeffsPerSet])
{
    const double T = static_cast<double>(kSegmentDurationS);
    const double inv_T7 = 1.0 / (T * T * T * T * T * T * T);
    double total = 0.0;
    for (int axis = 0; axis < kNumAxes; ++axis)
        for (int s = 0; s < kNumSegments; ++s)
            total += inv_T7 * segment_snap_integral(&set_coeffs[axis * kSysN + s * kCoeffsPerSegment]);
    return total;
}

// ---------------------------------------------------------------------------
// BatchChecks — the three residual categories the task's "CONSTRAINTS"
// gate measures, plus the cost-sanity summary. Every field is the WORST
// (max) value seen across the whole K-set, both-axes batch, so a single
// number tells you whether ANY set in 10,000 has a problem.
// ---------------------------------------------------------------------------
struct BatchChecks {
    double max_interp_m = 0.0;          // worst |solved position - waypoint|, meters
    double max_endpoint_deriv = 0.0;    // worst |velocity|,|accel|,|jerk| at the two trajectory ends
    double max_continuity_rel = 0.0;    // worst interior derivative jump, relative (floor 1)
    bool   all_finite = true;           // every coefficient and every cost value is finite
    bool   cost_nonneg = true;          // every set's snap cost is >= -kCostNonNegFloor slack
    double min_cost = 1e300, max_cost = -1e300;
};

// check_batch — re-evaluate EVERY set in the batch (both axes) against the
// mathematical definition of minimum-snap constraints (interpolation,
// endpoint derivatives, interior continuity) and the analytic snap cost.
// Runs once, after VERIFY has already established GPU==CPU, so checking
// only the GPU-produced coefficients exercises the actual shipped result.
static BatchChecks check_batch(int K, const float* waypoints, const float* coeffs)
{
    BatchChecks r;
    double dL[kMaxContinuityDeriv + 1], dR[kMaxContinuityDeriv + 1];

    for (int k = 0; k < K; ++k) {
        for (int axis = 0; axis < kNumAxes; ++axis) {
            const float* set_coeffs = &coeffs[static_cast<size_t>(k) * kCoeffsPerSet + axis * kSysN];

            for (int s = 0; s < kNumSegments; ++s) {
                const float* seg = &set_coeffs[s * kCoeffsPerSegment];
                for (int j = 0; j < kCoeffsPerSegment; ++j)
                    if (!std::isfinite(seg[j])) r.all_finite = false;

                eval_segment_derivs(seg, 0.0, dL);
                const double wp_start = waypoints[static_cast<size_t>(k) * kWaypointFloatsPerSet
                                                  + static_cast<size_t>(s) * 2 + axis];
                r.max_interp_m = std::max(r.max_interp_m, std::fabs(dL[0] - wp_start));

                eval_segment_derivs(seg, 1.0, dR);
                const double wp_end = waypoints[static_cast<size_t>(k) * kWaypointFloatsPerSet
                                                + static_cast<size_t>(s + 1) * 2 + axis];
                r.max_interp_m = std::max(r.max_interp_m, std::fabs(dR[0] - wp_end));

                if (s == 0)
                    for (int d = 1; d <= kMaxEndpointDeriv; ++d)
                        r.max_endpoint_deriv = std::max(r.max_endpoint_deriv, std::fabs(dL[d]));
                if (s == kNumSegments - 1)
                    for (int d = 1; d <= kMaxEndpointDeriv; ++d)
                        r.max_endpoint_deriv = std::max(r.max_endpoint_deriv, std::fabs(dR[d]));
            }

            for (int m = 0; m < kNumInterior; ++m) {
                eval_segment_derivs(&set_coeffs[m * kCoeffsPerSegment], 1.0, dL);
                eval_segment_derivs(&set_coeffs[(m + 1) * kCoeffsPerSegment], 0.0, dR);
                for (int d = 1; d <= kMaxContinuityDeriv; ++d) {
                    const double scale = std::max(1.0, std::max(std::fabs(dL[d]), std::fabs(dR[d])));
                    r.max_continuity_rel = std::max(r.max_continuity_rel, std::fabs(dL[d] - dR[d]) / scale);
                }
            }
        }

        const double cost = compute_snap_cost(&coeffs[static_cast<size_t>(k) * kCoeffsPerSet]);
        if (!std::isfinite(cost)) {
            r.all_finite = false;
        } else {
            if (cost < kCostNonNegFloor) r.cost_nonneg = false;
            r.min_cost = std::min(r.min_cost, cost);
            r.max_cost = std::max(r.max_cost, cost);
        }
    }
    return r;
}

// ---------------------------------------------------------------------------
// draw_line — Bresenham's integer line-drawing algorithm (the textbook
// 1965 midpoint form), used only to connect consecutive dense-sampled path
// points into a visible curve in the PGM artifact below. No sub-pixel
// anti-aliasing — this is a teaching artifact for "does the path look like
// a slalom", not a rendering demo.
// ---------------------------------------------------------------------------
static void draw_line(std::vector<uint8_t>& img, int w, int h, int x0, int y0, int x1, int y1, uint8_t val)
{
    int dx = std::abs(x1 - x0), sx = x0 < x1 ? 1 : -1;
    int dy = -std::abs(y1 - y0), sy = y0 < y1 ? 1 : -1;
    int err = dx + dy;
    for (;;) {
        if (x0 >= 0 && x0 < w && y0 >= 0 && y0 < h) img[static_cast<size_t>(y0) * w + x0] = val;
        if (x0 == x1 && y0 == y1) break;
        const int e2 = 2 * err;
        if (e2 >= dy) { err += dy; x0 += sx; }
        if (e2 <= dx) { err += dx; y0 += sy; }
    }
}

// write_pgm — the smallest real image format there is (P5: one header line
// trio, then raw bytes), viewable in any image tool, no libraries (the
// 07.09 idiom, reused verbatim).
static bool write_pgm(const std::string& path, int width, int height, const std::vector<uint8_t>& gray)
{
    std::ofstream out(path, std::ios::binary);
    if (!out.is_open()) return false;
    out << "P5\n" << width << " " << height << "\n255\n";
    out.write(reinterpret_cast<const char*>(gray.data()), static_cast<std::streamsize>(gray.size()));
    return static_cast<bool>(out);
}

// ---------------------------------------------------------------------------
// write_slalom_artifacts — dense-sample ONE waypoint set's trajectory
// (position, velocity, acceleration vs time, both axes) into
// demo/out/trajectory.csv, and rasterize the 2-D path into
// demo/out/slalom_path.pgm. samples_per_segment=50 at kSegmentDurationS=1s
// gives dt=0.02s per row — the repo's familiar 50 Hz sample spacing.
// ---------------------------------------------------------------------------
static bool write_slalom_artifacts(const std::string& out_dir,
                                   const float set_coeffs[kCoeffsPerSet],
                                   const float wp[kWaypointFloatsPerSet],
                                   int samples_per_segment,
                                   int* out_num_rows)
{
    if (!ensure_dir(out_dir)) return false;

    std::ofstream f(out_dir + "/trajectory.csv");
    if (!f.is_open()) return false;
    f << "t_s,x_m,y_m,vx_ms,vy_ms,ax_ms2,ay_ms2\n";   // units in the header row (CLAUDE.md §12)

    std::vector<std::pair<double, double>> pts;
    double d0[kMaxContinuityDeriv + 1], d1[kMaxContinuityDeriv + 1];
    int rows = 0;

    for (int s = 0; s < kNumSegments; ++s) {
        // Every segment samples tau in [0,1); the LAST segment also emits
        // tau=1 once, so the whole trajectory's final waypoint appears
        // exactly once in the file (not duplicated at every segment join).
        const int n = samples_per_segment + (s == kNumSegments - 1 ? 1 : 0);
        for (int i = 0; i < n; ++i) {
            const double tau = static_cast<double>(i) / samples_per_segment;
            const double t = (static_cast<double>(s) + tau) * static_cast<double>(kSegmentDurationS);
            eval_segment_derivs(&set_coeffs[0 * kSysN + s * kCoeffsPerSegment], tau, d0);   // x axis
            eval_segment_derivs(&set_coeffs[1 * kSysN + s * kCoeffsPerSegment], tau, d1);   // y axis
            const double x = d0[0], y = d1[0];
            const double vx = d0[1] / kSegmentDurationS, vy = d1[1] / kSegmentDurationS;
            const double ax = d0[2] / (kSegmentDurationS * kSegmentDurationS);
            const double ay = d1[2] / (kSegmentDurationS * kSegmentDurationS);
            f << t << ',' << x << ',' << y << ',' << vx << ',' << vy << ',' << ax << ',' << ay << '\n';
            pts.emplace_back(x, y);
            ++rows;
        }
    }
    if (!f) return false;
    if (out_num_rows) *out_num_rows = rows;

    // ---- PGM: rasterize the 2-D path, waypoints marked as bright crosses ---
    const int W = 256, H = 256;
    double minx = 1e300, maxx = -1e300, miny = 1e300, maxy = -1e300;
    for (int i = 0; i < kNumWaypoints; ++i) {
        minx = std::min(minx, static_cast<double>(wp[i * 2 + 0]));
        maxx = std::max(maxx, static_cast<double>(wp[i * 2 + 0]));
        miny = std::min(miny, static_cast<double>(wp[i * 2 + 1]));
        maxy = std::max(maxy, static_cast<double>(wp[i * 2 + 1]));
    }
    const double margin = 0.5;   // meters of padding so the path never touches the image edge
    minx -= margin; maxx += margin; miny -= margin; maxy += margin;
    const double spanx = std::max(maxx - minx, 1e-3), spany = std::max(maxy - miny, 1e-3);

    // World (x,y) -> pixel (px,py): y is FLIPPED so larger y draws higher on
    // the image (image row 0 is the top, but +y is "up" in our world frame).
    auto to_px = [&](double x, double y, int& px, int& py) {
        px = static_cast<int>((x - minx) / spanx * (W - 1) + 0.5);
        py = (H - 1) - static_cast<int>((y - miny) / spany * (H - 1) + 0.5);
    };

    std::vector<uint8_t> img(static_cast<size_t>(W) * H, 20);   // dark background
    int px0 = 0, py0 = 0;
    to_px(pts[0].first, pts[0].second, px0, py0);
    for (size_t i = 1; i < pts.size(); ++i) {
        int px1 = 0, py1 = 0;
        to_px(pts[i].first, pts[i].second, px1, py1);
        draw_line(img, W, H, px0, py0, px1, py1, 190);   // mid-gray path
        px0 = px1; py0 = py1;
    }
    for (int i = 0; i < kNumWaypoints; ++i) {   // bright 5x5 crosses at the actual waypoints
        int px = 0, py = 0;
        to_px(wp[i * 2 + 0], wp[i * 2 + 1], px, py);
        for (int dy = -2; dy <= 2; ++dy)
            for (int dx = -2; dx <= 2; ++dx) {
                const int xx = px + dx, yy = py + dy;
                if (xx >= 0 && xx < W && yy >= 0 && yy < H) img[static_cast<size_t>(yy) * W + xx] = 255;
            }
    }
    return write_pgm(out_dir + "/slalom_path.pgm", W, H, img);
}

// ---------------------------------------------------------------------------
// main — batch generation, GPU+CPU solve, the two verification stages, the
// artifact, and the final verdict (see the file header for the full flow).
// ---------------------------------------------------------------------------
int main(int argc, char** argv)
{
    int K = kDefaultBatch;
    std::string data_path;
    for (int i = 1; i < argc; ++i) {
        if      (!std::strcmp(argv[i], "--batch") && i + 1 < argc) K = std::atoi(argv[++i]);
        else if (!std::strcmp(argv[i], "--data")  && i + 1 < argc) data_path = argv[++i];
        else {
            std::fprintf(stderr,
                "usage: %s [--batch K] [--data waypoint_sets.csv]\n"
                "note: non-default K changes the PROBLEM line; the demo diff will flag it.\n",
                argv[0]);
            return 2;
        }
    }

    std::printf("[demo] Minimum-snap trajectory optimization batched over waypoint sets (project 15.01)\n");
    print_device_info();

    // ---- Sample stage: the hand-designed, named waypoint sets --------------
    // Loaded BEFORE the PROBLEM: line is printed, deliberately: the sample
    // sets always occupy the front of the batch (see below), so if the
    // caller requests a --batch smaller than the sample count, K must be
    // clamped UP first — the PROBLEM: line must always report the K the
    // rest of the run actually uses, never a stale pre-clamp value.
    const std::string sample_path = find_file(data_path, argv[0], "data/sample/waypoint_sets.csv");
    if (sample_path.empty()) {
        std::printf("PROBLEM: batched minimum-snap trajectories, K=%d waypoint sets requested, FP32\n", K);
        std::printf("SAMPLE: NOT FOUND — data/sample/waypoint_sets.csv missing (run scripts/make_synthetic.py?)\n");
        std::printf("RESULT: FAIL (sample data missing)\n");
        return 1;
    }
    SampleSets sample = load_sample_sets(sample_path);
    if (!sample.loaded) {
        std::printf("PROBLEM: batched minimum-snap trajectories, K=%d waypoint sets requested, FP32\n", K);
        std::printf("SAMPLE: MALFORMED — see stderr\n");
        std::printf("RESULT: FAIL (sample data malformed)\n");
        return 1;
    }
    int slalom_idx = -1;
    for (size_t i = 0; i < sample.names.size(); ++i)
        if (sample.names[i] == "slalom") { slalom_idx = static_cast<int>(i); break; }
    if (slalom_idx < 0) {
        std::printf("PROBLEM: batched minimum-snap trajectories, K=%d waypoint sets requested, FP32\n", K);
        std::printf("SAMPLE: MALFORMED — no 'slalom' set found (required for the artifact)\n");
        std::printf("RESULT: FAIL (sample data missing the slalom set)\n");
        return 1;
    }
    const int n_sample = static_cast<int>(sample.names.size());
    if (K < n_sample) {
        std::fprintf(stderr, "[info] --batch %d is smaller than the %d sample sets; using %d\n",
                    K, n_sample, n_sample);
        K = n_sample;
    }

    std::printf("PROBLEM: batched minimum-snap trajectories, K=%d waypoint sets, "
                "%d segments x %d coeffs/segment x %d axes = %d unknowns/set, "
                "%d waypoints/set, dt_seg=%.1f s (%.1f s total), box +/-%.1f m, min spacing %.2f m, FP32\n",
                K, kNumSegments, kCoeffsPerSegment, kNumAxes, kCoeffsPerSet, kNumWaypoints,
                static_cast<double>(kSegmentDurationS), static_cast<double>(kTrajDurationS),
                static_cast<double>(kBoxHalfExtentM), static_cast<double>(kMinSpacingM));
    std::printf("[info] sample file: %s\n", sample_path.c_str());
    std::printf("SAMPLE: loaded %d hand-designed waypoint set(s) from data/sample/waypoint_sets.csv, "
                "incl. 'slalom' [synthetic]\n", n_sample);

    // ---- Batch: sample sets first, then seeded-random fill ------------------
    std::vector<float> waypoints(static_cast<size_t>(K) * kWaypointFloatsPerSet);
    std::copy(sample.waypoints.begin(), sample.waypoints.end(), waypoints.begin());

    uint32_t rng = 0x9E3779B9u;   // fixed seed: reproducible batches, documented here and in data/README.md
    for (int k = n_sample; k < K; ++k)
        generate_random_waypoint_set(rng, kBoxHalfExtentM, kMinSpacingM,
                                     &waypoints[static_cast<size_t>(k) * kWaypointFloatsPerSet]);

    // ---- Persistent device buffers, GPU solve --------------------------------
    float *d_waypoints = nullptr, *d_coeffs = nullptr;
    const size_t wp_bytes = static_cast<size_t>(K) * kWaypointFloatsPerSet * sizeof(float);
    const size_t co_bytes = static_cast<size_t>(K) * kCoeffsPerSet * sizeof(float);
    CUDA_CHECK(cudaMalloc(&d_waypoints, wp_bytes));
    CUDA_CHECK(cudaMalloc(&d_coeffs, co_bytes));
    CUDA_CHECK(cudaMemcpy(d_waypoints, waypoints.data(), wp_bytes, cudaMemcpyHostToDevice));

    std::vector<float> coeffs_gpu(static_cast<size_t>(K) * kCoeffsPerSet);
    GpuTimer gt;
    gt.begin();
    launch_minsnap_batch(K, d_waypoints, d_coeffs);
    const float gpu_ms = gt.end_ms();
    CUDA_CHECK(cudaMemcpy(coeffs_gpu.data(), d_coeffs, co_bytes, cudaMemcpyDeviceToHost));
    CUDA_CHECK(cudaFree(d_waypoints));
    CUDA_CHECK(cudaFree(d_coeffs));

    // ---- CPU oracle, same batch -----------------------------------------------
    std::vector<float> coeffs_cpu(static_cast<size_t>(K) * kCoeffsPerSet);
    CpuTimer ct;
    ct.begin();
    minsnap_batch_cpu(K, waypoints.data(), coeffs_cpu.data());
    const double cpu_ms = ct.end_ms();

    std::printf("[time] batch (K=%d, %d unknowns/set): CPU %.1f ms | GPU kernel %.3f ms | speed-up %.0fx "
                "(teaching artifact; kernel only)\n",
                K, kCoeffsPerSet, cpu_ms, static_cast<double>(gpu_ms),
                cpu_ms / (static_cast<double>(gpu_ms) > 0.0 ? static_cast<double>(gpu_ms) : 1.0));

    // ======================= VERIFY: GPU vs CPU (the §5 gate) ================
    // Elementwise coefficient comparison, relative tolerance floored at 1
    // (08.01's pattern) — a real indexing/assembly/pivoting bug shifts
    // coefficients at O(1) relative scale, not at the FP32-rounding scale
    // this tolerance is sized for. THEORY.md justifies the exact number.
    bool verify_pass = true;
    float worst_rel = 0.0f;
    for (size_t i = 0; i < coeffs_gpu.size(); ++i) {
        const float g = coeffs_gpu[i], c = coeffs_cpu[i];
        const float scale = std::fabs(c) > 1.0f ? std::fabs(c) : 1.0f;
        const float d = std::fabs(g - c) / scale;
        if (d > worst_rel) worst_rel = d;
        if (d > kVerifyRelTol || !std::isfinite(g) || !std::isfinite(c)) verify_pass = false;
    }
    std::printf("[info] verify: worst relative coefficient deviation %.3e over %d sets x %d axes x %d coeffs\n",
                static_cast<double>(worst_rel), K, kNumAxes, kSysN);
    std::printf("VERIFY: %s (GPU coefficients match CPU reference within rel tol %.0e)\n",
                verify_pass ? "PASS" : "FAIL", static_cast<double>(kVerifyRelTol));
    if (!verify_pass) {
        std::printf("RESULT: FAIL (GPU/CPU coefficient disagreement — fix before trusting the batch)\n");
        return 1;
    }

    // ======================= CONSTRAINTS: verify against the DEFINITION ======
    // Independent (double-precision, separately-coded) re-check of every
    // set in the batch against the actual mathematical definition of
    // minimum-snap constraints — see check_batch's header comment for why
    // this is not redundant with VERIFY above.
    const BatchChecks bc = check_batch(K, waypoints.data(), coeffs_gpu.data());
    std::printf("[info] constraints: worst interpolation error %.3e m (tol %.0e m)\n",
                bc.max_interp_m, static_cast<double>(kTolInterpM));
    std::printf("[info] constraints: worst endpoint |derivative| %.3e (tol %.0e)\n",
                bc.max_endpoint_deriv, static_cast<double>(kTolEndpointDeriv));
    std::printf("[info] constraints: worst interior continuity jump (relative) %.3e (tol %.0e)\n",
                bc.max_continuity_rel, static_cast<double>(kTolContinuityRel));
    std::printf("[info] constraints: snap cost range over batch [%.4e, %.4e] (finite=%s, non-negative=%s)\n",
                bc.min_cost, bc.max_cost, bc.all_finite ? "yes" : "NO", bc.cost_nonneg ? "yes" : "NO");

    const bool constraints_pass = bc.all_finite && bc.cost_nonneg
                               && bc.max_interp_m <= static_cast<double>(kTolInterpM)
                               && bc.max_endpoint_deriv <= static_cast<double>(kTolEndpointDeriv)
                               && bc.max_continuity_rel <= static_cast<double>(kTolContinuityRel);
    std::printf("CONSTRAINTS: %s (interpolation/endpoint/continuity residuals and snap-cost sanity "
                "within documented tolerances for all %d sets)\n", constraints_pass ? "PASS" : "FAIL", K);

    // ======================= ARTIFACT: dense-sample the slalom set ===========
    const std::string out_dir = project_root_from(argv[0]) + "/demo/out";
    const float* slalom_coeffs = &coeffs_cpu[static_cast<size_t>(slalom_idx) * kCoeffsPerSet];
    const float* slalom_wp = &waypoints[static_cast<size_t>(slalom_idx) * kWaypointFloatsPerSet];
    int artifact_rows = 0;
    const bool artifact_ok = write_slalom_artifacts(out_dir, slalom_coeffs, slalom_wp, 50, &artifact_rows);
    if (artifact_ok)
        std::printf("ARTIFACT: wrote demo/out/trajectory.csv and demo/out/slalom_path.pgm (%d samples)\n",
                    artifact_rows);
    else
        std::printf("ARTIFACT: FAILED to write demo/out files\n");

    // ======================= RESULT ============================================
    const bool success = verify_pass && constraints_pass && artifact_ok;
    if (success)
        std::printf("RESULT: PASS (batch verified against CPU reference and against the minimum-snap "
                    "constraint definition; artifact written)\n");
    else
        std::printf("RESULT: FAIL (see VERIFY / CONSTRAINTS / ARTIFACT lines above)\n");
    return success ? 0 : 1;
}
